				<p>Once in a record, Click on the slide image to open slide viewer<br />
If there’s more than one slide, you can view them in separate window, in the same window,
or in parallel view.<br />
In parallel view you can manipulate each slide separately or by clicking on the padlock icon,
you can lock them all and manipulate them together.<br />
To increase magnification, you can either scroll in and out using your mouse scroller, or
press the plus and minus buttons, or set magnification buttons in the toolbar.<br />
To rotate the slide, you can use the rotation tool in the top right corner of each slide or the
rotation tool or set rotation buttons in the toolbar.<br />
To take an image of the slides, click on the camera button.<br />
To annotate the slide, click on the pencil button and select what you’d like to use to
annotate. You can also change the size and colour of your annotation.<br />
To add measurements, click on the measure tool and apply to the slide.<br />
You can hide and show the annotations by clicking on the eye button.<br />
Clicking on the compass button hides and shows the smaller slide image
Click on the graph to bring up the slide properties.<br />
To view the slides in full screen, click on the arrows button. To return to normal screen,
press escape on your keyboard
You can remove slides form the screen by clicking on the folder button and then the X
button next to the slide you want to remove.<br />
Clicking on the I button shows you an image of the actual slide and slide label
To return to the case click ‘view case’</p>

